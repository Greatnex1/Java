package CHP_5;

public class Looping {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.print(i + " ");
        }

          System.out.println();

        for (int i = 10; i >= 1; i--) {
            System.out.print(i + " ");
        }
        System.out.println();
        for (int i = 1; i <= 21; i+=2) {
            System.out.print(i + " ");
        }

    }

}
