package CHP_5;

public class TrianglePrinting {
    public static void main(String[] args) {
        for (int i = 0; i <= 0; i++) {
            System.out.println(" * \n **\n ***\n ****\n *****\n ****** \n ******* \n ******** \n *********\n");
        }

        for (int j = 0; j < 1; j++) {
            System.out.println(" *************\n ************\n **********\n *********\n ******** \n ******\n *****\n ****\n ***\n **\n *");
        }

    }
}
