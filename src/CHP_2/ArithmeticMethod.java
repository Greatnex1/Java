package CHP_2;

public class ArithmeticMethod {

    public int calculateArithmetic(int firstNumber){
        int square = (firstNumber * firstNumber);
        return  square;
    }
public int calculateArithmeticSecond(int secondNumber){
        int square1 = (secondNumber * secondNumber);
        return square1;
}
 public int calculateArithmeticSum(int firstNumber, int secondNumber){
     return firstNumber + secondNumber;
 }
public int calculateArithmeticDifference(int firstNumber, int secondNumber){
        return firstNumber - secondNumber;

}
}
